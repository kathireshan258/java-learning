package dataTypes;

public class MinMax {
    public static void main(String[] args) {
        System.out.println("Byte Min Range:" + Byte.MIN_VALUE + " Byte Max Range:" + Byte.MAX_VALUE);
        System.out.println("Short Min Range:" + Short.MIN_VALUE + " Short Max Range:" + Short.MAX_VALUE);
        System.out.println("Integer Min Range:" + Integer.MIN_VALUE + " Integer Max Range:" + Integer.MAX_VALUE);
        System.out.println("Long Min Range:" + Long.MIN_VALUE + " Long Max Range:" + Long.MAX_VALUE);
        System.out.println("Size of Byte:" + Byte.SIZE);
        System.out.println("Size of Short:" + Short.SIZE);
        System.out.println("Size of Integer:" + Integer.SIZE);
        System.out.println("Size of Long:" + Long.SIZE);
    }
}